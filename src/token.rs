use fraction::Fraction;

#[derive(Debug, Clone, PartialEq)]
pub struct Token {
    pub kind: TokenKind,
    pub line: usize,
    pub column: usize,
}

#[derive(Debug, Clone, PartialEq)]
pub enum TokenKind {
    Immutable,
    Mutable,
    Colon,
    Identifier(String),
    String(String),
    Number(Fraction),
    Bool(bool),
    Void,
    Equal,
    Plus,
    Minus,
    Mul,
    Div,
    Mod,
    Pow,
    LParen,
    RParen,
    LBrace,
    RBrace,
    Eof,
    Function,
    BackSlash,
    Pipe,
    Return,
    Break,
    Continue,
    Comma,
    RArrow,
    Match,
    LBrancket,
    RBrancket,
    RRocket,
    If,
    Else,
    Eq,
    Neq,
    Lte,
    Lt,
    Gte,
    Gt,
    True,
    False,
    And,
    Or,
    Xor,
    Struct,
    Pub,
    Dot,
    Impl,
    CommentBlock(String),
    CommentLine(String),
    For,
    In,
    Import,
    From,
    Option,
    Some,
    None,
    Result,
    Success,
    Failure,
    List,
    Dict,
}
