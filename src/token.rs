use fraction::Fraction;

#[derive(Debug, Clone, PartialEq)]
pub struct Token {
    pub kind: TokenKind,
    pub line: usize,
    pub column: usize,
}

#[derive(Debug, Clone, PartialEq)]
pub enum TokenKind {
    Immutable,
    Mutable,
    Colon,
    Identifier(String),
    String(String),
    Number(Fraction),
    Void,
    Equal,
    Plus,
    Minus,
    Mul,
    Div,
    Mod,
    LParen,
    RParen,
    LBrace,
    RBrace,
    Eof,
    Function,
    BackSlash,
    Pipe,
    Return,
    Comma,
    RArrow,
    Match,
    LBrancket,
    RBrancket,
    RRocket,
    If,
    Else,
    Eq,
    Lte,
    Lt,
    Gte,
    Gt,
    Struct,
    Pub,
    Dot,
    Impl,
    CommentBlock(String),
    CommentLine(String),
    For,
    In,
    Import,
    From,
    Option,
    Some,
    None,
}
